/* --------------------------------------------------
    Below is an example of a String. Strings can be
    anything (i.e. number, letters, special
    character, etc.) as long as they are encapsulated
    (surrounded) by quotes. These can be single
    quotes or double quotes but they must be the
    same.

    String Example: "Scrumfluence"
    String Example: "1J8GP28K28W135557"
    String Example: "0000005"
-------------------------------------------------- */

let name = "Scrumfluence"; 
console.log(typeof name, "-->", name);



/* --------------------------------------------------
    Below is an example of an Integer. Integers can
    be any whole number (positive or negative)
    between -9007199254740991 to +9007199254740991.

    Integer Example: 5
    Integer Example: -20
    Integer Example: 23415445001515
-------------------------------------------------- */

let age = 15;
console.log(typeof age, "-->", age);



/* --------------------------------------------------
    Below is an example of a Boolean. Booleans can
    only be true or false.

    Boolean Example: true
    Boolean Example: false
-------------------------------------------------- */

let isEmployed = true;
console.log(typeof isEmployed, "-->", isEmployed);
